/* 
+ : addition
- : substraction
* : multiplication
/ : division
% : modulus(reminder)
** : exponent
*/

//ADDITION
let add = 10+99
console.log(add);
console.log();

//SUBSTRACTION
let subs = 10 - 99;
console.log(subs);
console.log();

//MULTIPLICATION
let mul = 10 * 99;
console.log(mul);
console.log();

//DIVISION
let div = 10/99
console.log(div);
console.log();

//MODULUS(REMAINDER)
let mod = 10 % 99;
console.log(mod);
console.log();

//EXPONENT
let exp = 4 ** 4
console.log(exp);
console.log();







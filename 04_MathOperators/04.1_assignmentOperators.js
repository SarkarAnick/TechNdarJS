/* 
++ : increment y 1
-- : decrement by 1
+= : increment by value
-+ : decrement by value 
*= : multiply by value
/= : divide by value
%= : modulus by value
**= : exponent by value
*/
let points = 10;
points++; // points = points + 1
console.log(points);

points--;
console.log(points);

points += 10;
console.log(points);

points -= 20;
console.log(points);

let age = 7;
age *= 2;
console.log(age);

age /= 2;
console.log(age);

age %= 2; // age = age%2
console.log(age);

age **= 2
console.log(age);


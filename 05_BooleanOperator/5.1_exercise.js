/* Write the truth tables of AND , OR , NOT using two boolean values , one on each side
    
    use console.log to log out the results for each
    HINT: AND and OR will have 4 possibilities and NOT will have 2
*/
console.log("-----AND-----");
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

console.log("-----OR-----");
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

console.log("-----NOT-----");
console.log(!true);
console.log(!false);


